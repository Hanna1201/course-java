package lessons.two;

public class Task03 {
    public static void main(String[] args) {
        double r = 7;
        final double PI = 3.14;
        double p = 2 * PI * r;
        double s = PI * r * r;
        System.out.println("Длина окружности круга равна " + p);
        System.out.println("Площадь круга равна " + s);
    }
}
